def conform_to_contract(df: pd.DataFrame, file_name: str) -> pd.DataFrame:
    headers = CSV_HEADERS[file_name]
    out = df.copy()
    for col in headers:
        if col not in out.columns:
            out[col] = np.nan
    return out.reindex(columns=headers)


def write_template(path: Path, file_name: str) -> None:
    path.parent.mkdir(parents=True, exist_ok=True)
    if path.exists() and path.stat().st_size > 0:
        return
    pd.DataFrame(columns=CSV_HEADERS[file_name]).to_csv(path, index=False)
